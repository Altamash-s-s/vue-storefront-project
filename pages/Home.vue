    const wishlistItemsQty = computed(() => wishlistStore.wishlist?.items_count ?? 0);



    const accountIcon = computed(() => (isAuthenticated.value ? 'profile_fill' : 'profile'));
    const handleAccountClick = async () => {
      if (isAuthenticated.value) {
        await router.push(app.localeRoute({ name: 'customer-my-profile' }));
      } else {
        toggleLoginModal();
      }
    };

    onMounted(async () => {
      if (app.$device.isDesktop) {
        await loadCartTotalQty();
        // eslint-disable-next-line promise/catch-or-return
        await loadWishlistItemsCount();

      }


    });

    useFetch(async () => {
      page.value = await loadPage({ identifier: 'home' });
      // AOS.init()
    });




    onMounted(() => {
      addTags([{ prefix: CacheTagPrefix.View, value: 'home' }]);

      (function () {

        //--------------------------- Snap Scroll Code START

        var selector = ".snap";

        var $slides = $(selector);

        var currentSlide = 0;
        var isAnimating = false;

        var stopAnimation = function () {
          setTimeout(function () {
            isAnimating = false;
          }, 0);
        };

        var bottomIsReached = function ($elem) {
          var rect = $elem[0].getBoundingClientRect();
          return rect.bottom <= $(window).height();
        };

        var topIsReached = function ($elem) {
          var rect = $elem[0].getBoundingClientRect();
          return rect.top >= 0; 
        };

        document.addEventListener(
          "wheel",
          function (event) {
            var $currentSlide = $($slides[currentSlide]);

            if (isAnimating) {
              event.preventDefault();
              return;
            }

            var direction = -event.deltaY;

            if (direction < 0) {
              // next
              if (currentSlide + 1 >= $slides.length) return;
              if (!bottomIsReached($currentSlide)) return;
              event.preventDefault();
              currentSlide++;
              var $slide = $($slides[currentSlide]);
              var offsetTop = $slide.offset().top;
              isAnimating = true;

              $("html, body").animate(
                {
                  scrollTop: offsetTop - 0
                },
              1000, 
                stopAnimation
              );
            } else {
              // back
              if (currentSlide - 1 < 0) return;
              if (!topIsReached($currentSlide)) return;
              event.preventDefault();
              currentSlide--;
              var $slide = $($slides[currentSlide]);
              var offsetTop = $slide.offset().top;
              isAnimating = true;
              $("html, body").animate(
                {
                  scrollTop: offsetTop - 0
                },
               1000, 
                stopAnimation
              );
            }
          },
          { passive: false }
        );


        //--------------------------- Snap Scroll Code END


        var parallax, speed;
        parallax = document.querySelectorAll('.parallax-image');
        speed = 0.3;
        window.onscroll = function () {


          return [].slice.call(parallax).forEach(function (el, i) {
            var dist;
            dist = $(window).scrollTop() - $(el).offset().top;
            return $(el).css('top', dist * speed + 'px');
          });


        };


      }).call(this);

      $(document).ready(function () {


        var header = $('.homepage_header');
        var menu = document.querySelector(".open_menu");
        var logo = document.querySelector(".sf-header__logo");
        var icons = document.querySelector(".sf-header__actions");
        var sticky = header.offsetTop;

        $(window).scroll(function(){

          var scroll = $(window).scrollTop();
          if (scroll >= 200) {
            $(header).addClass("sticky");
            $(menu).addClass("show_hd_dtls");
            $(logo).addClass("show_hd_dtls");
            $(icons).addClass("show_hd_dtls");
          }
          else {
            $(header).removeClass("sticky");
            $(menu).removeClass("show_hd_dtls");
            $(logo).removeClass("show_hd_dtls");
            $(icons).removeClass("show_hd_dtls");
          }
      });


        $(".humb_menu_btn").click(function () {
          $('.menu_popup').css('display', 'block');
          setTimeout(function () {
            $('.menu_col').css('opacity', '1');
          }, 200);
        });

        $(".close_btn").click(function () {

          $('.menu_col').css('opacity', '0');
          setTimeout(function () {
            $('.menu_popup').css('display', 'none');
          }, 200);

        });

        $('#audio-control').click(function () {
          if ($("#banner_video").prop('muted')) {
            $("#banner_video").prop('muted', false);
            $('.mute_icon').show();
            $('.unmute_icon').hide();

          } else {
            $("#banner_video").prop('muted', true);
            $('.mute_icon').hide();
            $('.unmute_icon').show();
          }
        });

        const videoElement = document.querySelector('#banner_video') as HTMLVideoElement;
        const playPauseButton = document.querySelector('.video-control') as HTMLButtonElement;

        playPauseButton.addEventListener('click', () => {
          playPauseButton.classList.toggle('playing');
          if (playPauseButton.classList.contains('playing')) {
            videoElement.pause();
          }
          else {
            videoElement.play();
          }
        });

        videoElement.addEventListener('ended', () => {
          playPauseButton.classList.remove('playing');
        });

        window.addEventListener('load', videoScroll);
        window.addEventListener('scroll', videoScroll);

        function videoScroll() {
          var videoElements = document.querySelectorAll('#banner_video[autoplay]');

          if (videoElements.length > 0) {
            var windowHeight = window.innerHeight;

            videoElements.forEach(function (thisVideoEl) {
              var videoElement = thisVideoEl as HTMLVideoElement; // Cast to HTMLVideoElement
              var videoHeight = videoElement.clientHeight;
              var videoClientRect = videoElement.getBoundingClientRect().top;

              if (videoClientRect <= (windowHeight - videoHeight * 0.10) && videoClientRect >= -videoHeight * 0.10) {
                videoElement.play();
              } else {
                videoElement.pause();
              }
            });
          }
        }


      });






    });

    // @ts-ignore
    return {
      // banners,
      // callToAction,
      // hero,
      page,
      accountIcon,
      cartTotalItems: computed(() => cart.value?.total_quantity ?? 0),
      handleAccountClick,
      isAuthenticated,
      toggleCartSidebar,
      toggleWishlistSidebar,




    };
  },
  head() {
    return getMetaInfo(this.page);

  },
